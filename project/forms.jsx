const Select = ({ children, value: ctrlVal, defaultValue, onChange, disabled, style }) => {
  const controlled  = ctrlVal !== undefined;
  const [inner, setInner] = React.useState(defaultValue !== undefined ? String(defaultValue) : '');
  const current  = controlled ? String(ctrlVal ?? '') : inner;
  const [open, setOpen] = React.useState(false);
  const ref = React.useRef(null);

  const opts = React.Children.toArray(children)
    .filter(c => c?.props)
    .map(c => ({ value: String(c.props.value ?? ''), label: c.props.children, disabled: !!c.props.disabled }));

  const selected = opts.find(o => o.value === current) || opts[0];
  const isEmpty  = !current || current === '' || selected?.value === '';

  React.useEffect(() => {
    if (!open) return;
    const fn = e => { if (ref.current && !ref.current.contains(e.target)) setOpen(false); };
    document.addEventListener('mousedown', fn);
    return () => document.removeEventListener('mousedown', fn);
  }, [open]);

  const pick = (opt) => {
    if (opt.disabled) return;
    if (!controlled) setInner(opt.value);
    onChange?.({ target: { value: opt.value } });
    setOpen(false);
  };

  const chevron = `url("data:image/svg+xml;utf8,<svg xmlns='http://www.w3.org/2000/svg' width='11' height='6' viewBox='0 0 11 6'><path fill='%23999' d='M0 0h11L5.5 6z'/></svg>")`;

  return (
    <div ref={ref} style={{position:'relative', ...(style||{})}}>
      <button type="button" disabled={!!disabled} onClick={()=>!disabled&&setOpen(v=>!v)} style={{
        width:'100%', height:46, padding:'0 36px 0 14px',
        border:`1.5px solid ${open?'var(--accent)':'var(--border)'}`,
        borderRadius:10, background:'var(--surface)',
        color: isEmpty ? 'var(--ink-3)' : 'var(--ink)',
        fontFamily:'var(--font-km),var(--font-en),sans-serif',
        fontSize:16, lineHeight:1.5,
        textAlign:'left', cursor: disabled ? 'not-allowed' : 'pointer',
        outline:'none', display:'flex', alignItems:'center',
        backgroundImage:chevron, backgroundRepeat:'no-repeat',
        backgroundPosition:'right 13px center',
        opacity: disabled ? 0.4 : 1,
        overflow:'hidden', whiteSpace:'nowrap',
        transition:'border-color .12s',
      }}>
        <span style={{overflow:'hidden',textOverflow:'ellipsis',whiteSpace:'nowrap'}}>
          {selected?.label ?? '---'}
        </span>
      </button>

      {open && (
        <div style={{
          position:'absolute', top:'calc(100% + 4px)', left:0, right:0, zIndex:9999,
          background:'var(--surface)',
          border:'1.5px solid var(--accent)',
          borderRadius:10,
          boxShadow:'0 8px 32px rgba(0,0,0,.14)',
          maxHeight:260, overflowY:'auto',
        }}>
          {opts.map((opt, i) => {
            const isSelected = opt.value === current;
            const isPlaceholder = opt.value === '';
            const isDisabled = opt.disabled;
            return (
              <div key={i} onClick={()=>!isDisabled&&pick(opt)} style={{
                padding:'11px 14px',
                background: isSelected ? 'var(--accent-soft)' : 'transparent',
                color: isDisabled ? 'var(--ink-3)' : isSelected ? 'var(--accent)' : isPlaceholder ? 'var(--ink-3)' : 'var(--ink)',
                fontFamily:'var(--font-km),var(--font-en),sans-serif',
                fontSize:16, lineHeight:1.6,
                cursor: isDisabled ? 'not-allowed' : isPlaceholder ? 'default' : 'pointer',
                userSelect:'none',
                borderTop: i > 0 ? '1px solid var(--border)' : 'none',
                fontWeight: isSelected ? 600 : 400,
                opacity: isDisabled ? 0.45 : 1,
              }}
                onMouseEnter={e=>{ if(!isPlaceholder&&!isSelected&&!isDisabled) e.currentTarget.style.background='var(--surface-muted)'; }}
                onMouseLeave={e=>{ e.currentTarget.style.background=isSelected?'var(--accent-soft)':'transparent'; }}
              >
                {opt.label}
              </div>
            );
          })}
        </div>
      )}
    </div>
  );
};

// ComboSelect: editable input + dropdown list — pick from list OR type custom value
// Uses a broadcast event so only one instance can be open at a time.
const _CS_EV = '__csOpen';

const ComboSelect = ({ children, options: optsProp, value, onChange, placeholder, style, disabled }) => {
  const id = React.useRef(Math.random().toString(36).slice(2)).current;

  const opts = React.useMemo(() => {
    if (children) {
      return React.Children.toArray(children).filter(c=>c?.props)
        .map(c => ({ value: String(c.props.value ?? c.props.children ?? ''), label: String(c.props.children ?? '') }));
    }
    return (optsProp||[]).map(o =>
      typeof o === 'string' ? {value:o, label:o}
        : {value:String(o.v||o.value||''), label:String(o.l||o.label||o.v||o.value||'')}
    );
  }, [children, optsProp]);

  const [open, setOpen] = React.useState(false);
  const [text, setText] = React.useState(value || '');
  const blurTimer = React.useRef(null);
  const inputRef = React.useRef(null);

  React.useEffect(() => { setText(value || ''); }, [value]);

  // When another ComboSelect broadcasts open, close this one
  React.useEffect(() => {
    const fn = e => { if (e.detail !== id) { clearTimeout(blurTimer.current); setOpen(false); } };
    window.addEventListener(_CS_EV, fn);
    return () => window.removeEventListener(_CS_EV, fn);
  }, []);

  const broadcast = () => window.dispatchEvent(new CustomEvent(_CS_EV, { detail: id }));

  const openDropdown = () => {
    clearTimeout(blurTimer.current);
    setOpen(true);
    broadcast();
  };

  // Delay close so option onClick fires before blur removes the list
  const scheduleClose = () => { blurTimer.current = setTimeout(() => setOpen(false), 150); };

  const pick = (opt) => {
    setText(opt.label);
    onChange?.(opt.value);
    clearTimeout(blurTimer.current);
    setOpen(false);
  };

  const handleInput = (e) => { setText(e.target.value); onChange?.(e.target.value); };

  // Separate layout props (wrapper div) from appearance props (input)
  const { flex, flexGrow, flexShrink, flexBasis, width: ww, minWidth, maxWidth, alignSelf, ...appearStyle } = style || {};
  const wrapperExtra = Object.fromEntries(
    Object.entries({ flex, flexGrow, flexShrink, flexBasis, width: ww, minWidth, maxWidth, alignSelf })
      .filter(([,v]) => v !== undefined)
  );

  const inpStyle = {
    width:'100%', height:46, padding:'0 14px',
    border:`1.5px solid ${open ? 'var(--accent)' : 'var(--border)'}`,
    borderRadius:10, fontSize:16,
    background:'var(--surface)', color:'var(--ink)',
    fontFamily:'var(--font-km),var(--font-en),sans-serif',
    outline:'none', boxSizing:'border-box',
    ...appearStyle,
    paddingRight:'36px',
  };

  const accentSoft = 'color-mix(in oklch,var(--accent) 12%,var(--surface))';

  return (
    <div style={{position:'relative', ...wrapperExtra}}>
      <input
        ref={inputRef} value={text} onChange={handleInput}
        placeholder={placeholder} disabled={disabled} style={inpStyle}
        onFocus={openDropdown}
        onBlur={scheduleClose}
      />
      <button type="button" tabIndex={-1}
        onMouseDown={e => {
          e.preventDefault(); // keep focus on the input
          clearTimeout(blurTimer.current);
          if (open) { setOpen(false); }
          else { setOpen(true); broadcast(); }
        }}
        style={{
          position:'absolute', right:0, top:0, height:'100%', width:36,
          background:'transparent', border:'none', cursor:'pointer', outline:'none',
          display:'flex', alignItems:'center', justifyContent:'center',
        }}>
        <svg width="10" height="6" viewBox="0 0 10 6"
          style={{transition:'transform .15s', transform:open?'rotate(180deg)':'none'}}>
          <path fill="currentColor" opacity=".45" d="M0 0h10L5 6z"/>
        </svg>
      </button>
      {open && opts.length > 0 && (
        <div style={{
          position:'absolute', top:'calc(100% + 4px)', left:0, right:0, zIndex:300,
          background:'var(--surface)', border:'1.5px solid var(--accent)',
          borderRadius:10, overflow:'hidden auto', maxHeight:260,
          boxShadow:'0 6px 24px rgba(0,0,0,.14)',
        }}>
          {opts.map((opt, i) => {
            const sel = opt.value === value || opt.label === text;
            return (
              <div key={i}
                onMouseDown={e => e.preventDefault()} // prevent input blur
                onClick={() => pick(opt)}
                style={{
                  padding:'10px 14px',
                  background: sel ? accentSoft : 'transparent',
                  color: sel ? 'var(--accent)' : 'var(--ink)',
                  fontFamily:'var(--font-km),var(--font-en),sans-serif',
                  fontSize:15, lineHeight:1.5, cursor:'pointer', userSelect:'none',
                  borderTop: i > 0 ? '1px solid var(--border)' : 'none',
                  fontWeight: sel ? 600 : 400,
                }}
                onMouseEnter={e => e.currentTarget.style.background='var(--surface-muted)'}
                onMouseLeave={e => e.currentTarget.style.background=sel?accentSoft:'transparent'}
              >
                {opt.label}
              </div>
            );
          })}
        </div>
      )}
    </div>
  );
};
